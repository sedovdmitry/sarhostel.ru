    <p class="toggle text-faded">
    Исторически это что-то вроде сочетания американских мотелей, русских доходных домов и европейских апартаментов, предлагавшие еще многие сотни лет назад неприхотливым путешественникам доступное место для ночлега.
    </p>
    <p class="toggle text-faded">
    Первый хостел ничем, по сути, не отличающийся от современных, был основан в Германии в 1909 году. Школьному учителю Рихарду Ширманну потребовалось вывозить на выходные дни и каникулы своих учеников за город. Бедные школьники не могли себе позволить денег на ночёвку. Группе школьников приходилось ночевать в школах поблизости, которые были закрыты на каникулы. Потихоньку у школьного учителя родилась идея постоянного бюджетного размещения для молодых людей. Самое первое подобное молодёжное общежитие устроил в школе, в которой и работал. По вечерам учитель со своими учениками относили стулья и парты из классов, на полу стелили мешки с соломой. По утрам гости помогали Ширманну выносить непритязательный вариант коек и установить на место парты и стулья. После этот хостел расположился в замке Алтена, самое интересное, что он там есть до сих пор.
